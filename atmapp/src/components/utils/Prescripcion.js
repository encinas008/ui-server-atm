export default class Prescripcion {
    constructor() {
        this.token = ""
        this.fur = ""
    }

    setToken(token_p) {
        this.token = token_p
    }

    getToken() {
        return this.token_p
    }

    setFur(fur) {
        this.fur = fur
    }

    getFur() {
        return this.fur
    }
}
